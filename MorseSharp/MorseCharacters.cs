            Language.Italiano => GetMorseCharactersItaliano(),
            Language.Japanese => GetMorseCharactersJapanese(),
            Language.Portugues => GetMorseCharactersPortugues(),
            _ => throw new ArgumentOutOfRangeException(nameof(Language))
        };
